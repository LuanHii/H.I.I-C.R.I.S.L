'use client';

import React from 'react';
import * as TooltipPrimitive from '@radix-ui/react-tooltip';
import { cn } from '@/lib/utils';

interface TooltipProps {
    content: React.ReactNode;
    children: React.ReactNode;
    side?: 'top' | 'right' | 'bottom' | 'left';
    align?: 'start' | 'center' | 'end';
    delayDuration?: number;
    className?: string;
}

export function Tooltip({
    content,
    children,
    side = 'top',
    align = 'center',
    delayDuration = 300,
    className,
}: TooltipProps) {
    return (
        <TooltipPrimitive.Provider delayDuration={delayDuration}>
            <TooltipPrimitive.Root>
                <TooltipPrimitive.Trigger asChild>
                    {children}
                </TooltipPrimitive.Trigger>
                <TooltipPrimitive.Portal>
                    <TooltipPrimitive.Content
                        side={side}
                        align={align}
                        sideOffset={5}
                        className={cn(
                            'z-50 overflow-hidden rounded-lg border border-ordem-border bg-ordem-black-deep px-3 py-2 text-xs text-ordem-white shadow-xl',
                            'animate-in fade-in-0 zoom-in-95',
                            'data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=closed]:zoom-out-95',
                            'data-[side=bottom]:slide-in-from-top-2',
                            'data-[side=left]:slide-in-from-right-2',
                            'data-[side=right]:slide-in-from-left-2',
                            'data-[side=top]:slide-in-from-bottom-2',
                            className
                        )}
                    >
                        {content}
                        <TooltipPrimitive.Arrow className="fill-ordem-black-deep" />
                    </TooltipPrimitive.Content>
                </TooltipPrimitive.Portal>
            </TooltipPrimitive.Root>
        </TooltipPrimitive.Provider>
    );
}

// Tooltip mais elaborado com t√≠tulo
interface RichTooltipProps extends TooltipProps {
    title?: string;
}

export function RichTooltip({
    title,
    content,
    children,
    side = 'top',
    align = 'center',
    delayDuration = 300,
    className,
}: RichTooltipProps) {
    return (
        <TooltipPrimitive.Provider delayDuration={delayDuration}>
            <TooltipPrimitive.Root>
                <TooltipPrimitive.Trigger asChild>
                    {children}
                </TooltipPrimitive.Trigger>
                <TooltipPrimitive.Portal>
                    <TooltipPrimitive.Content
                        side={side}
                        align={align}
                        sideOffset={5}
                        className={cn(
                            'z-50 overflow-hidden rounded-lg border border-ordem-border bg-ordem-black-deep shadow-xl max-w-xs',
                            'animate-in fade-in-0 zoom-in-95',
                            className
                        )}
                    >
                        {title && (
                            <div className="px-3 py-2 border-b border-ordem-border/50 font-bold text-sm text-white">
                                {title}
                            </div>
                        )}
                        <div className="px-3 py-2 text-xs text-ordem-text-secondary">
                            {content}
                        </div>
                        <TooltipPrimitive.Arrow className="fill-ordem-black-deep" />
                    </TooltipPrimitive.Content>
                </TooltipPrimitive.Portal>
            </TooltipPrimitive.Root>
        </TooltipPrimitive.Provider>
    );
}
